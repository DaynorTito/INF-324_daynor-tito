        <h2>Certificado Catastral</h2>
        <p>
        La Certificación Catastral se constituye en el único documento oficial, por el cual el Gobierno Autónomo Municipal de La Paz brinda a los ciudadanos seguridad jurídica respecto al registro técnico de sus inmuebles, consignando los datos físicos, económicos y jurídicos del mismo. El Certificado de Registro Catastral es el documento técnico de carácter público que especifica la ubicación geográfica georreferenciada de un inmueble, sus características físicas, tipología, su valoración económica, uso de suelo, identificación mediante un código catastral y datos de sus propietarios.
    </p>
    <p>
    Actualmente, de los 104.328 predios registrados en la Base de Catastro, 77.587 cuentan con Certificado de Registro Catastral.

